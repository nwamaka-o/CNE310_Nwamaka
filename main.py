def row_row_row_your_boat():
    print("Row, row, row your boat,")
    print("Gently down the stream.")
    
def life_is_a_dream():
    print("Merrily, merrily, merrily, merrily,")
    print("Life is but a dream.")

def first_verse():
    row_row_row_your_boat()
    life_is_a_dream()

def second_verse():
    print("Row, row, row your boat,")
    print("Gently down the stream.")
    print("If you see a crocodile,")
    print("Don't forget to scream!")

def third_verse():
    print("Row, row, row your boat,")
    print("Gently down the shore.")
    print("If you see a lion there,")
    print("Don't forget to roar!")

def play_song():
    first_verse()
    second_verse()
    third_verse()

# Test the code by calling play_song
if __name__ == "__main__":
    play_song()
